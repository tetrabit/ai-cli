function Get-AiCliConfigPath {
    if ($env:AI_CLI_CONFIG) { return $env:AI_CLI_CONFIG }
    if ($env:XDG_CONFIG_HOME) { return (Join-Path $env:XDG_CONFIG_HOME "ai-cli/config") }
    return (Join-Path $HOME ".config/ai-cli/config")
}

function Get-AiCliDataDir {
    if ($env:XDG_DATA_HOME) { return (Join-Path $env:XDG_DATA_HOME "ai-cli") }
    if ($env:LOCALAPPDATA) { return (Join-Path $env:LOCALAPPDATA "ai-cli") }
    return (Join-Path $HOME ".local/share/ai-cli")
}

function Get-AiCliConfigKey($Category, $ToolName) {
    return ("{0}_{1}" -f $Category, $ToolName).ToUpperInvariant()
}

function Get-AiCliConfig {
    $path = Get-AiCliConfigPath
    $config = @{}
    if (-not (Test-Path $path)) { return $config }

    foreach ($line in Get-Content $path) {
        $trimmed = $line.Trim()
        if (-not $trimmed -or $trimmed.StartsWith("#")) { continue }
        $idx = $trimmed.IndexOf("=")
        if ($idx -le 0) { continue }
        $key = $trimmed.Substring(0, $idx)
        $value = $trimmed.Substring($idx + 1)
        $config[$key] = $value
    }
    return $config
}

function Test-AiCliEnabled($Category, $ToolName) {
    $config = Get-AiCliConfig
    $key = Get-AiCliConfigKey $Category $ToolName
    $value = if ($config.ContainsKey($key)) { "$($config[$key])".ToLowerInvariant() } else { "1" }

    switch ($value) {
        { $_ -in @("1", "true", "yes", "y", "on") } { return $true }
        { $_ -in @("0", "false", "no", "n", "off") } { return $false }
        default { return $true }
    }
}

function Read-AiCliYesNo($Label, $Current) {
    $suffix = if ($Current) { "[Y/n]" } else { "[y/N]" }
    while ($true) {
        $answer = Read-Host ("  {0,-34} {1}" -f $Label, $suffix)
        switch -Regex ($answer) {
            "^\s*$" { return [bool]$Current }
            "^(y|yes)$" { return $true }
            "^(n|no)$" { return $false }
            default { Write-Host "Please answer yes or no." -ForegroundColor Yellow }
        }
    }
}

function Invoke-AiCliSetup {
    $path = Get-AiCliConfigPath
    $lines = @(
        "# ai-cli configuration",
        "# Generated by: ai setup",
        ""
    )

    Write-Host "ai-cli setup" -ForegroundColor Cyan
    Write-Host "Choose which tools ai-cli should install or update by default."
    Write-Host "Config: $path"
    Write-Host ""

    Write-Host "Tools installed or updated by 'ai update':" -ForegroundColor Cyan
    foreach ($toolInfo in $UpdateTools) {
        $toolKey = $toolInfo["Key"]
        $toolLabel = $toolInfo["Label"]
        $key = Get-AiCliConfigKey "update" $toolKey
        $enabled = Read-AiCliYesNo $toolLabel (Test-AiCliEnabled "update" $toolKey)
        $enabledValue = if ($enabled) { 1 } else { 0 }
        $lines += ("{0}={1}" -f $key, $enabledValue)
    }

    $lines += ""
    Write-Host ""
    Write-Host "Usage checks used by 'ai usage':" -ForegroundColor Cyan
    foreach ($toolInfo in $UsageTools) {
        $toolKey = $toolInfo["Key"]
        $toolLabel = $toolInfo["Label"]
        $key = Get-AiCliConfigKey "usage" $toolKey
        $enabled = Read-AiCliYesNo $toolLabel (Test-AiCliEnabled "usage" $toolKey)
        $enabledValue = if ($enabled) { 1 } else { 0 }
        $lines += ("{0}={1}" -f $key, $enabledValue)
    }

    $dir = Split-Path -Parent $path
    if ($dir) { New-Item -ItemType Directory -Force -Path $dir | Out-Null }
    $lines | Set-Content -Path $path -Encoding UTF8
    Write-Host ""
    Write-Host "Saved setup to $path" -ForegroundColor Green
}

function Get-WingetVersion($PackageId) {
    $lines = winget list --id $PackageId --disable-interactivity 2>$null
    foreach ($line in $lines) {
        if ($line -match $PackageId) {
            if ($line -match '(\d+\.\d+[\.\d]*)') { return $Matches[1] }
        }
    }
    return $null
}

function Update-WingetPackage($DisplayName, $PackageId) {
    Write-Host "==> Checking $DisplayName..." -ForegroundColor Cyan
    $ver = Get-WingetVersion $PackageId
    if (-not $ver) {
        Write-Host "  Not installed" -ForegroundColor Yellow
        return
    }
    $output = winget upgrade $PackageId --disable-interactivity 2>&1 | Out-String
    if ($output -match "No available upgrade found|No newer package") {
        Write-Host "  Already up to date ($ver)" -ForegroundColor Green
    } else {
        $newVer = Get-WingetVersion $PackageId
        if ($newVer) {
            Write-Host "  Updated $ver -> $newVer" -ForegroundColor Yellow
        } else {
            Write-Host "  Updated successfully" -ForegroundColor Yellow
        }
    }
}

function Update-GhCopilot {
    Write-Host "==> Checking GitHub Copilot CLI..." -ForegroundColor Cyan
    $output = gh copilot update 2>&1 | Out-String
    if ($output -match "No update needed.*current version is ([^\s,]+)") {
        Write-Host "  Already up to date ($($Matches[1]))" -ForegroundColor Green
    } elseif ($output -match "Updated|updated|Updating") {
        Write-Host "  Updated successfully" -ForegroundColor Yellow
    } else {
        Write-Host "  Already up to date" -ForegroundColor Green
    }
}

function Update-Antigravity {
    Write-Host "==> Checking Antigravity CLI..." -ForegroundColor Cyan
    $oldVer = $null
    if (Get-Command agy -ErrorAction SilentlyContinue) {
        $oldVer = agy --version 2>$null | Select-Object -First 1
    } else {
        Write-Host "  Not installed, installing..." -ForegroundColor Yellow
    }

    try {
        $installer = Invoke-RestMethod -Uri "https://antigravity.google/cli/install.ps1" -UseBasicParsing
        $scriptBlock = [scriptblock]::Create($installer)
        & $scriptBlock -NonInteractive
    } catch {
        Write-Host "  Installation failed: $_" -ForegroundColor Yellow
        return
    }

    if (-not (Get-Command agy -ErrorAction SilentlyContinue)) {
        Write-Host "  Installed, but agy is not on PATH yet. Restart your shell and try again." -ForegroundColor Yellow
        return
    }

    $newVer = agy --version 2>$null | Select-Object -First 1
    if (-not $oldVer) {
        if ($newVer) {
            Write-Host "  Installed ($newVer)" -ForegroundColor Green
        } else {
            Write-Host "  Installed" -ForegroundColor Green
        }
    } elseif ($oldVer -eq $newVer) {
        Write-Host "  Already up to date ($newVer)" -ForegroundColor Green
    } else {
        Write-Host "  Updated $oldVer -> $newVer" -ForegroundColor Yellow
    }
}

function Remove-LegacyNpmPackages($DisplayName, $Package, $Binary, [string[]]$LegacyPackages) {
    foreach ($legacyPackage in $LegacyPackages) {
        if (-not $legacyPackage -or $legacyPackage -eq $Package) { continue }
        $legacy = (npm list -g $legacyPackage --depth=0 --json 2>$null | ConvertFrom-Json).dependencies.$legacyPackage.version
        if (-not $legacy) { continue }

        $suffix = if ($Binary) { " to free $Binary" } else { "" }
        Write-Host "  Removing legacy $DisplayName $legacy from $legacyPackage$suffix." -ForegroundColor Yellow
        npm uninstall -g $legacyPackage --loglevel error
    }
}

function Update-NpmPackage($DisplayName, $Package, $Binary = $null, [string[]]$LegacyPackages = @()) {
    Write-Host "==> Checking $DisplayName..." -ForegroundColor Cyan
    $current = (npm list -g $Package --depth=0 --json 2>$null | ConvertFrom-Json).dependencies.$Package.version
    $latest  = (npm view $Package version 2>$null).Trim()
    if (-not $current) {
        Write-Host "  Not installed, installing $latest..." -ForegroundColor Yellow
        Remove-LegacyNpmPackages $DisplayName $Package $Binary $LegacyPackages
        npm install -g "${Package}@latest" --loglevel error
    } elseif ($current -eq $latest) {
        Write-Host "  Already up to date ($current)" -ForegroundColor Green
        Remove-LegacyNpmPackages $DisplayName $Package $Binary $LegacyPackages
    } else {
        Write-Host "  Updating $current -> $latest..." -ForegroundColor Yellow
        Remove-LegacyNpmPackages $DisplayName $Package $Binary $LegacyPackages
        npm install -g "${Package}@latest" --loglevel error
    }
}

function Install-WingetDependency($DisplayName, $PackageId) {
    if (-not (Get-Command winget -ErrorAction SilentlyContinue)) {
        Write-Host "  $DisplayName is missing and winget was not found." -ForegroundColor Yellow
        return $false
    }

    Write-Host "  $DisplayName is missing, installing..." -ForegroundColor Yellow
    winget install --id $PackageId --exact --silent --accept-package-agreements --accept-source-agreements | Out-Null
    return ($LASTEXITCODE -eq 0)
}

function Ensure-CommandDependency($CommandName, $DisplayName, $PackageId) {
    if (Get-Command $CommandName -ErrorAction SilentlyContinue) { return $true }
    if (-not (Install-WingetDependency $DisplayName $PackageId)) { return $false }
    return [bool](Get-Command $CommandName -ErrorAction SilentlyContinue)
}

function Ensure-BunDependency {
    $bunBin = Join-Path $HOME ".bun\bin"
    if ((Test-Path $bunBin) -and (($env:PATH -split [IO.Path]::PathSeparator) -notcontains $bunBin)) {
        $env:PATH = "$bunBin$([IO.Path]::PathSeparator)$env:PATH"
    }
    if (Get-Command bun -ErrorAction SilentlyContinue) { return $true }
    if (-not (Install-WingetDependency "Bun" "Oven-sh.Bun")) { return $false }
    return [bool](Get-Command bun -ErrorAction SilentlyContinue)
}

function Ensure-JustDependency {
    if (Get-Command just -ErrorAction SilentlyContinue) { return $true }
    if ($script:AiCliJustChecked) { return $true }
    $script:AiCliJustChecked = $true
    if (-not (Install-WingetDependency "just" "Casey.Just")) {
        Write-Host "  just is still missing; Pi vs Claude Code dependencies can install, but recipes will not run until just is installed." -ForegroundColor Yellow
        return $true
    }
    if (-not (Get-Command just -ErrorAction SilentlyContinue)) {
        Write-Host "  just is still missing; Pi vs Claude Code dependencies can install, but recipes will not run until just is installed." -ForegroundColor Yellow
    }
    return $true
}

function Ensure-UpdateDependencies {
    Write-Host "==> Checking ai-cli dependencies..." -ForegroundColor Cyan
    $ok = $true

    if ((Test-AiCliEnabled "update" "codex") -or (Test-AiCliEnabled "update" "pi") -or (Test-AiCliEnabled "update" "omx")) {
        if (-not (Ensure-CommandDependency "npm" "Node.js/npm" "OpenJS.NodeJS.LTS")) { $ok = $false }
    }
    if ((Test-AiCliEnabled "update" "gh_cli") -or (Test-AiCliEnabled "update" "copilot")) {
        if (-not (Ensure-CommandDependency "gh" "GitHub CLI" "GitHub.cli")) { $ok = $false }
    }
    if ((Test-AiCliEnabled "update" "pi_vs_claude_code") -or (Test-AiCliEnabled "update" "hermes")) {
        if (-not (Ensure-CommandDependency "git" "Git" "Git.Git")) { $ok = $false }
    }
    if (Test-AiCliEnabled "update" "pi_vs_claude_code") {
        if (-not (Ensure-BunDependency)) { $ok = $false }
        Ensure-JustDependency | Out-Null
    }

    if ($ok) {
        Write-Host "  Dependencies ready." -ForegroundColor Green
    }
    return $ok
}

function Invoke-AiCliDoctor {
    Write-Host "==> Checking ai-cli launcher..." -ForegroundColor Cyan
    Write-Host "  Windows installs do not use sudo. Re-run the installer from an elevated shell if ai.ps1 needs repair." -ForegroundColor Yellow

    Write-Host ""
    $dependenciesOk = Ensure-UpdateDependencies

    Write-Host ""
    Write-Host "==> Checking npm package handoffs..." -ForegroundColor Cyan
    if (Get-Command npm -ErrorAction SilentlyContinue) {
        Remove-LegacyNpmPackages "Pi Coding Agent" "@earendil-works/pi-coding-agent" "pi" @("@mariozechner/pi-coding-agent")
        Write-Host "  npm package handoffs checked." -ForegroundColor Green
    } else {
        Write-Host "  npm not found; skipping npm package handoff checks." -ForegroundColor Yellow
    }

    Write-Host ""
    if (Test-AiCliEnabled "update" "omx") {
        Write-Host "==> Checking Oh My Codex..." -ForegroundColor Cyan
        if (Get-Command omx -ErrorAction SilentlyContinue) {
            $omxVer = $null
            try { $omxVer = (omx --version 2>$null | Select-Object -First 1) } catch {}
            if ($omxVer) {
                Write-Host "  Oh My Codex: $omxVer" -ForegroundColor Green
            } else {
                Write-Host "  Oh My Codex: installed" -ForegroundColor Green
            }
            try {
                omx doctor >$null 2>&1
                if ($LASTEXITCODE -ne 0) {
                    Write-Host "  omx doctor reported issues; run 'omx doctor' for details." -ForegroundColor Yellow
                }
            } catch {}
        } else {
            Write-Host "  omx not found" -ForegroundColor Yellow
            if (Get-Command npm -ErrorAction SilentlyContinue) {
                Write-Host "    Install with: npm install -g oh-my-codex" -ForegroundColor Yellow
            }
        }
        Write-Host ""
    }

    if ($dependenciesOk) {
        Write-Host "Doctor checks complete." -ForegroundColor Green
    } else {
        Write-Host "Doctor found issues it could not fix." -ForegroundColor Yellow
        exit 1
    }
}

