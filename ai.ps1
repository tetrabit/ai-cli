function Get-GitHead($RepoDir) {
    $head = git -C $RepoDir rev-parse --short HEAD 2>$null
    if ($LASTEXITCODE -eq 0) { return (($head | Select-Object -First 1) -as [string]) }
    return $null
}

function Update-PiVsClaudeCode {
    Write-Host "==> Checking Pi vs Claude Code..." -ForegroundColor Cyan

    $repoUrl = "https://github.com/disler/pi-vs-claude-code.git"
    $repoDir = Get-PiVsClaudeCodeDir
    $gitDir = Join-Path $repoDir ".git"

    if (-not (Ensure-CommandDependency "git" "Git" "Git.Git")) {
        Write-Host "  Git is required to install this repo." -ForegroundColor Yellow
        return
    }

    if (Test-Path $gitDir) {
        $current = Get-GitHead $repoDir
        git -C $repoDir pull --ff-only *> $null
        if ($LASTEXITCODE -ne 0) {
            Write-Host "  Update failed. Run git pull manually in $repoDir for details." -ForegroundColor Yellow
            return
        }

        $updated = Get-GitHead $repoDir
        if ($current -and $current -eq $updated) {
            Write-Host "  Repo already up to date ($updated)" -ForegroundColor Green
        } else {
            $displayCurrent = if ($current) { $current } else { "unknown" }
            $displayUpdated = if ($updated) { $updated } else { "unknown" }
            Write-Host "  Updated repo $displayCurrent -> $displayUpdated" -ForegroundColor Yellow
        }
    } elseif (Test-Path $repoDir) {
        Write-Host "  $repoDir exists but is not a git checkout. Set AI_CLI_PI_VS_CLAUDE_CODE_DIR to another path." -ForegroundColor Yellow
        return
    } else {
        $parent = Split-Path -Parent $repoDir
        if ($parent) { New-Item -ItemType Directory -Force -Path $parent | Out-Null }
        Write-Host "  Not installed, cloning..." -ForegroundColor Yellow
        git clone $repoUrl $repoDir *> $null
        if ($LASTEXITCODE -ne 0) {
            Write-Host "  Clone failed. Run git clone manually for details." -ForegroundColor Yellow
            return
        }
        Write-Host "  Cloned to $repoDir" -ForegroundColor Green
    }

    if (-not (Ensure-BunDependency)) {
        Write-Host "  Bun is required for dependencies." -ForegroundColor Yellow
        return
    }

    bun install --cwd $repoDir *> $null
    if ($LASTEXITCODE -ne 0) {
        Write-Host "  Dependency install failed. Run 'bun install --cwd `"$repoDir`"' for details." -ForegroundColor Yellow
        return
    }

    Ensure-JustDependency | Out-Null
    if (Get-Command just -ErrorAction SilentlyContinue) {
        Write-Host "  Dependencies installed. Run recipes from $repoDir with 'just'." -ForegroundColor Green
    } else {
        Write-Host "  Dependencies installed. Install 'just' to use the bundled recipes in $repoDir." -ForegroundColor Yellow
    }
}

function Add-HermesPathForCurrentSession {
    $candidates = @(
        (Join-Path $env:LOCALAPPDATA "hermes\hermes-agent\venv\Scripts"),
        (Join-Path $env:LOCALAPPDATA "hermes\hermes-agent")
    )
    $pathItems = $env:Path -split ";"

    foreach ($candidate in $candidates) {
        if ($candidate -and (Test-Path $candidate) -and ($pathItems -notcontains $candidate)) {
            $env:Path = "$candidate;$env:Path"
            $pathItems = $env:Path -split ";"
        }
    }
}

function Get-HermesVersion {
    if (-not (Get-Command hermes -ErrorAction SilentlyContinue)) {
        return $null
    }

    $verOutput = hermes --version 2>$null | Select-Object -First 1
    if (-not $verOutput) {
        $verOutput = hermes version 2>$null | Select-Object -First 1
    }
    if ($verOutput -match 'v?(\d+\.\d+\.\d+)') {
        return $Matches[1]
    }
    return $null
}

function Install-Hermes {
    $installUrl = "https://raw.githubusercontent.com/NousResearch/hermes-agent/main/scripts/install.ps1"

    try {
        $installer = Invoke-RestMethod -Uri $installUrl -UseBasicParsing
        $scriptBlock = [scriptblock]::Create($installer)
        & $scriptBlock -SkipSetup -NonInteractive
    } catch {
        Write-Host "  Installation failed: $_" -ForegroundColor Yellow
        return $false
    }

    Add-HermesPathForCurrentSession
    return $true
}

function Update-Hermes {
    Write-Host "==> Checking Hermes Agent..." -ForegroundColor Cyan
    if (-not (Ensure-CommandDependency "git" "Git" "Git.Git")) { return }

    if (-not (Get-Command hermes -ErrorAction SilentlyContinue)) {
        Write-Host "  Not installed, installing..." -ForegroundColor Yellow
        if (-not (Install-Hermes)) {
            return
        }

        if (-not (Get-Command hermes -ErrorAction SilentlyContinue)) {
            Write-Host "  Installed, but hermes is not on PATH yet. Restart your shell and try again." -ForegroundColor Yellow
            return
        }

        $installed = Get-HermesVersion
        if ($installed) {
            Write-Host "  Installed ($installed)" -ForegroundColor Green
        } else {
            Write-Host "  Installed" -ForegroundColor Green
        }
        return
    }

    $current = Get-HermesVersion
    $startVer = $current
    $updated = $false

    $hadCi = Test-Path Env:CI
    $previousCi = $env:CI
    try {
        # Hermes updates reinstall Node dependencies whose postinstall hooks may
        # print cosmetic demos directly to the terminal. CI=true suppresses
        # those demos without disabling required install scripts.
        if (-not $env:CI) { $env:CI = "true" }
        for ($i = 0; $i -lt 8; $i++) {
            hermes update >$null 2>&1
            $newVer = Get-HermesVersion
            if (-not $newVer) { break }
            if ($current -and $newVer -eq $current) { break }
            $updated = $true
            $current = $newVer
        }
    } finally {
        if ($hadCi) {
            $env:CI = $previousCi
        } else {
            Remove-Item Env:CI -ErrorAction SilentlyContinue
        }
    }

    if ($updated -and $current) {
        $displayCurrent = if ($startVer) { $startVer } else { "unknown" }
        Write-Host "  Updated $displayCurrent -> $current" -ForegroundColor Yellow
    } elseif ($current) {
        Write-Host "  Already up to date ($current)" -ForegroundColor Green
    } else {
        Write-Host "  Update check complete" -ForegroundColor Green
    }
}

