render_usage_bar() {
    python3 - "$1" <<'PY'
import sys

try:
    percent = float(sys.argv[1])
except Exception:
    percent = 0.0

segments = 20
ratio = max(0.0, min(1.0, percent / 100.0))
filled = min(segments, max(0, int(round(ratio * segments))))
print("[" + "#" * filled + "-" * (segments - filled) + "]")
PY
}

