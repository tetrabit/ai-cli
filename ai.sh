canonical_file_path() {
    local path="$1"
    local dir base

    [[ -n "$path" ]] || return 1
    if [[ "$path" == */* ]]; then
        dir="${path%/*}"
        base="${path##*/}"
        [[ -n "$dir" ]] || dir="/"
    else
        dir="."
        base="$path"
    fi

    (
        cd -P -- "$dir" >/dev/null 2>&1
        printf '%s/%s\n' "$PWD" "$base"
    )
}

current_script_path() {
    local source="${BASH_SOURCE[0]:-$0}"

    [[ -n "$source" ]] || return 1
    if [[ "$source" != /* ]]; then
        source="$PWD/$source"
    fi
    canonical_file_path "$source"
}

doctor_install_file() {
    local source="$1"
    local dest="$2"
    local mode="$3"
    local dest_dir parent
    local use_sudo=false

    dest_dir="$(dirname "$dest")"
    parent="$(dirname "$dest_dir")"

    if [[ ! -d "$dest_dir" ]]; then
        if [[ -w "$parent" ]]; then
            if ! mkdir -p "$dest_dir"; then
                return 1
            fi
        else
            use_sudo=true
        fi
    fi

    if [[ -d "$dest_dir" ]]; then
        if [[ ! -w "$dest_dir" || ( -e "$dest" && ! -w "$dest" ) ]]; then
            use_sudo=true
        fi
    fi

    if $use_sudo; then
        if ! prompt_for_sudo "update $dest"; then
            return 1
        fi
        if ! sudo mkdir -p "$dest_dir"; then
            return 1
        fi
        sudo install -m "$mode" "$source" "$dest"
    else
        install -m "$mode" "$source" "$dest"
    fi
}

