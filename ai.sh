do_usage() {
    usage_ran=false
    run_selected_usage claude usage_claude
    run_selected_usage codex usage_codex
    run_selected_usage antigravity usage_antigravity
    run_selected_usage copilot usage_copilot

    if ! $usage_ran; then
        echo -e "${YELLOW}No usage providers selected. Run 'ai setup' to choose providers.${NC}"
    else
        echo ""
        echo -e "${GREEN}Usage check complete.${NC}"
    fi
}

run_selected_usage() {
    local tool="$1"
    shift

    if ai_cli_enabled usage "$tool"; then
        if $usage_ran; then
            echo ""
        fi
        "$@"
        usage_ran=true
    fi
}

do_update() {
    local dependency_ok=true
    update_ran=false

    ensure_update_dependencies || dependency_ok=false
    echo ""

    run_selected_update claude check_claude
    run_selected_update gh_cli check_gh_cli
    run_selected_update copilot check_gh_copilot
    run_selected_update antigravity check_antigravity
    run_selected_update codex check_npm_package "Codex CLI" "@openai/codex" "codex"
    run_selected_update pi check_npm_package "Pi Coding Agent" "@earendil-works/pi-coding-agent" "pi" "@mariozechner/pi-coding-agent"
    run_selected_update pi_vs_claude_code check_pi_vs_claude_code
    run_selected_update hermes check_hermes
    run_selected_update omx check_omx

    if ! $update_ran; then
        echo -e "${YELLOW}No update tools selected. Run 'ai setup' to choose tools.${NC}"
    else
        echo ""
        if $dependency_ok; then
            echo -e "${GREEN}All selected AI tools checked.${NC}"
        else
            echo -e "${YELLOW}All selected AI tools checked, but some dependencies could not be installed.${NC}"
            return 1
        fi
    fi
}

run_selected_update() {
    local tool="$1"
    shift

    if ai_cli_enabled update "$tool"; then
        if $update_ran; then
            echo ""
        fi
        "$@"
        update_ran=true
    fi
}

run_claude() {
    local uid
    uid="${EUID:-$(id -u)}"

    if [[ "$uid" -eq 0 ]]; then
        exec env IS_SANDBOX=1 CLAUDE_CODE_BUBBLEWRAP=1 claude --dangerously-skip-permissions "$@"
    fi

    exec claude --dangerously-skip-permissions "$@"
}

tool="${1:-}"
shift 2>/dev/null || true

case "$tool" in
    claude)  run_claude "$@" ;;
    codex)   exec codex --yolo "$@" ;;
    antigravity|agy) exec agy --dangerously-skip-permissions "$@" ;;
    copilot) exec gh copilot --yolo "$@" ;;
    pi)      exec pi "$@" ;;
    hermes)  exec hermes --yolo "$@" ;;
    update)  do_update ;;
    doctor)  do_doctor ;;
    usage)   do_usage ;;
    setup)   do_setup ;;
    *)
        echo "Usage: ai <tool> [extra args]"
        echo "  ai claude   -> claude --dangerously-skip-permissions"
        echo "  ai codex    -> codex --yolo"
        echo "  ai antigravity -> agy --dangerously-skip-permissions"
        echo "  ai copilot  -> gh copilot --yolo"
        echo "  ai pi       -> pi"
        echo "  ai hermes   -> hermes --yolo"
        echo "  ai update   -> update all AI tools"
        echo "  ai doctor   -> diagnose and repair ai-cli install issues"
        echo "  ai usage    -> show remaining usage by provider"
        echo "  ai setup    -> choose tools for update and usage checks"
        echo ""
        echo "Options:"
        echo "  ai update --verbose  -> show full output from all tools"
        ;;
esac
