#!/usr/bin/env bash
set -euo pipefail

CYAN='\033[0;36m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
RED='\033[0;31m'
NC='\033[0m'

VERBOSE=false
for arg in "$@"; do
    if [[ "$arg" == "--verbose" || "$arg" == "-v" ]]; then
        VERBOSE=true
    fi
done
# Strip --verbose / -v from args
args=()
for arg in "$@"; do
    [[ "$arg" == "--verbose" || "$arg" == "-v" ]] || args+=("$arg")
done
set -- "${args[@]+"${args[@]}"}"

path_has_dir() {
    local dir="$1"
    case ":$PATH:" in
        *":$dir:"*) return 0 ;;
        *) return 1 ;;
    esac
}

prepend_path_dir() {
    local dir="$1"
    local item new_path old_ifs

    [[ -d "$dir" ]] || return 0
    new_path="$dir"
    old_ifs="$IFS"
    IFS=:
    for item in ${PATH:-}; do
        [[ -n "$item" && "$item" != "$dir" ]] || continue
        new_path="${new_path}:$item"
    done
    IFS="$old_ifs"
    PATH="$new_path"
}

npm_config_prefix() {
    npm config get prefix 2>/dev/null | head -n 1 || true
}

npm_prefix_is_under_home() {
    local prefix="$1"
    [[ -n "$prefix" && "$prefix" == "$HOME"/* ]]
}

npm_user_prefix() {
    if [[ -n "${AI_CLI_NPM_PREFIX:-}" ]]; then
        printf '%s\n' "$AI_CLI_NPM_PREFIX"
        return
    fi

    local configured_prefix
    configured_prefix="$(npm_config_prefix)"
    if npm_prefix_is_under_home "$configured_prefix"; then
        printf '%s\n' "$configured_prefix"
    else
        printf '%s\n' "$HOME/.local"
    fi
}

npm_user_bin() {
    printf '%s/bin\n' "$(npm_user_prefix)"
}

prepend_path_dir "$(npm_user_bin)"

AI_CLI_UPDATE_TOOLS=(claude gh_cli copilot antigravity codex pi pi_vs_claude_code hermes omx)
AI_CLI_USAGE_TOOLS=(claude codex antigravity copilot)

ai_cli_data_dir() {
    if [[ -n "${XDG_DATA_HOME:-}" ]]; then
        printf '%s/ai-cli\n' "$XDG_DATA_HOME"
    else
        printf '%s/.local/share/ai-cli\n' "$HOME"
    fi
}

ai_cli_config_file() {
    if [[ -n "${AI_CLI_CONFIG:-}" ]]; then
        printf '%s\n' "$AI_CLI_CONFIG"
    elif [[ -n "${XDG_CONFIG_HOME:-}" ]]; then
        printf '%s/ai-cli/config\n' "$XDG_CONFIG_HOME"
    else
        printf '%s/.config/ai-cli/config\n' "$HOME"
    fi
}

ai_cli_config_key() {
    local category="$1"
    local tool="$2"

    printf '%s_%s\n' \
        "$(printf '%s' "$category" | tr '[:lower:]' '[:upper:]')" \
        "$(printf '%s' "$tool" | tr '[:lower:]' '[:upper:]')"
}

ai_cli_config_get() {
    local key="$1"
    local default_value="${2:-1}"
    local config value

    config="$(ai_cli_config_file)"
    if [[ -f "$config" ]]; then
        value=$(awk -F= -v key="$key" '
            $1 == key {
                value = $0
                sub(/^[^=]*=/, "", value)
            }
            END {
                if (value != "") {
                    print value
                }
            }
        ' "$config")
        if [[ -n "$value" ]]; then
            printf '%s\n' "$value"
            return
        fi
    fi

    printf '%s\n' "$default_value"
}

ai_cli_enabled() {
    local category="$1"
    local tool="$2"
    local value

    value=$(ai_cli_config_get "$(ai_cli_config_key "$category" "$tool")" "1")
    value=$(printf '%s' "$value" | tr '[:upper:]' '[:lower:]')

    case "$value" in
        1|true|yes|y|on) return 0 ;;
        0|false|no|n|off) return 1 ;;
        *) return 0 ;;
    esac
}

ai_cli_tool_label() {
    case "$1" in
        claude)  printf 'Claude Code' ;;
        gh_cli)  printf 'GitHub CLI (Copilot dependency)' ;;
        copilot) printf 'GitHub Copilot CLI' ;;
        antigravity) printf 'Antigravity CLI' ;;
        codex)   printf 'Codex CLI' ;;
        pi)      printf 'Pi Coding Agent' ;;
        pi_vs_claude_code) printf 'Pi vs Claude Code' ;;
        hermes)  printf 'Hermes Agent' ;;
        omx)     printf 'Oh My Codex' ;;
        *)       printf '%s' "$1" ;;
    esac
}

prompt_yes_no() {
    local label="$1"
    local current="$2"
    local prompt answer

    if [[ "$current" == "1" ]]; then
        prompt="[Y/n]"
    else
        prompt="[y/N]"
    fi

    while true; do
        printf '  %-34s %s ' "$label" "$prompt"
        IFS= read -r answer || answer=""
        case "$answer" in
            "")
                [[ "$current" == "1" ]]
                return
                ;;
            [Yy]|[Yy][Ee][Ss])
                return 0
                ;;
            [Nn]|[Nn][Oo])
                return 1
                ;;
            *)
                echo "Please answer yes or no."
                ;;
        esac
    done
}

do_setup() {
    local config tmp tool key current enabled
    local -a lines=()

    config="$(ai_cli_config_file)"

    echo -e "${CYAN}ai-cli setup${NC}"
    echo "Choose which tools ai-cli should install or update by default."
    echo "Config: $config"
    echo ""

    lines+=("# ai-cli configuration")
    lines+=("# Generated by: ai setup")
    lines+=("")

    echo -e "${CYAN}Tools installed or updated by 'ai update':${NC}"
    for tool in "${AI_CLI_UPDATE_TOOLS[@]}"; do
        key="$(ai_cli_config_key update "$tool")"
        if ai_cli_enabled update "$tool"; then
            current=1
        else
            current=0
        fi
        if prompt_yes_no "$(ai_cli_tool_label "$tool")" "$current"; then
            enabled=1
        else
            enabled=0
        fi
        lines+=("${key}=${enabled}")
    done

    lines+=("")
    echo ""
    echo -e "${CYAN}Usage checks used by 'ai usage':${NC}"
    for tool in "${AI_CLI_USAGE_TOOLS[@]}"; do
        key="$(ai_cli_config_key usage "$tool")"
        if ai_cli_enabled usage "$tool"; then
            current=1
        else
            current=0
        fi
        if prompt_yes_no "$(ai_cli_tool_label "$tool")" "$current"; then
            enabled=1
        else
            enabled=0
        fi
        lines+=("${key}=${enabled}")
    done

    mkdir -p "$(dirname "$config")"
    tmp="${config}.tmp.$$"
    {
        for line in "${lines[@]}"; do
            printf '%s\n' "$line"
        done
    } > "$tmp"
    mv "$tmp" "$config"

    echo ""
    echo -e "${GREEN}Saved setup to $config${NC}"
}

ensure_managed_block_in_file() {
    local file="$1"
    local block="$2"
    local dir

    dir="$(dirname "$file")"
    mkdir -p "$dir"
    touch "$file"
    python3 - "$file" "$block" <<'PY'
import pathlib
import sys

path = pathlib.Path(sys.argv[1])
block = sys.argv[2]
start = "# >>> ai-cli PATH >>>"
end = "# <<< ai-cli PATH <<<"
managed = f"{start}\n{block}\n{end}\n"

content = path.read_text() if path.exists() else ""
normalized = content.rstrip("\n")

if start in content and end in content:
    prefix, remainder = content.split(start, 1)
    _, suffix = remainder.split(end, 1)
    replacement = prefix.rstrip("\n")
    if replacement:
        replacement += "\n\n"
    replacement += managed
    suffix = suffix.lstrip("\n")
    if suffix:
        replacement += "\n" + suffix
    updated = replacement
else:
    updated = normalized
    if updated:
        updated += "\n\n"
    updated += managed

if not updated.endswith("\n"):
    updated += "\n"

changed = updated != content
path.write_text(updated)
sys.exit(0 if changed else 1)
PY
}

persist_user_bin_path() {
    local bin_dir shell_name block
    local -a files=()
    local updated=false

    bin_dir="$(npm_user_bin)"
    block="export PATH=\"$bin_dir:\$PATH\""
    shell_name="$(basename "${SHELL:-}")"

    case "$shell_name" in
        bash)
            files+=("$HOME/.bashrc" "$HOME/.profile")
            ;;
        zsh)
            files+=("$HOME/.zshrc" "$HOME/.zprofile")
            ;;
        fish)
            ensure_managed_block_in_file "$HOME/.config/fish/config.fish" "fish_add_path \"$bin_dir\"" && updated=true
            ;;
    esac

    files+=("$HOME/.profile")

    local file
    for file in "${files[@]}"; do
        ensure_managed_block_in_file "$file" "$block" && updated=true
    done

    $updated
}

read_npm_package_version() {
    local package="$1"
    local prefix="${2:-}"
    local -a cmd=(npm list -g "$package" --depth=0 --json)
    if [[ -n "$prefix" ]]; then
        cmd+=(--prefix "$prefix")
    fi
    "${cmd[@]}" 2>/dev/null | python3 -c "import sys,json; print(json.load(sys.stdin).get('dependencies',{}).get('$package',{}).get('version',''))" 2>/dev/null || true
}

read_command_version() {
    local binary="$1"
    local output version

    if ! command -v "$binary" >/dev/null 2>&1; then
        return 1
    fi

    output=$("$binary" --version 2>/dev/null || "$binary" -v 2>/dev/null || true)
    version=$(printf '%s\n' "$output" | grep -oE '[0-9]+\.[0-9]+\.[0-9]+' | head -1 || true)
    [[ -n "$version" ]] || return 1
    printf '%s\n' "$version"
}

npm_global_install_is_writable() {
    local prefix global_root

    prefix="$(npm_config_prefix)"
    [[ -n "$prefix" ]] || return 1

    global_root="$prefix/lib/node_modules"
    if [[ -d "$global_root" ]]; then
        [[ -w "$global_root" ]]
    else
        [[ -d "$prefix" && -w "$prefix" ]]
    fi
}

npm_install_prefix() {
    local configured_prefix
    if [[ -n "${AI_CLI_NPM_PREFIX:-}" ]]; then
        printf '%s\n' "$AI_CLI_NPM_PREFIX"
        return
    fi

    configured_prefix="$(npm_config_prefix)"
    if npm_global_install_is_writable && [[ -n "$configured_prefix" ]]; then
        printf '%s\n' "$configured_prefix"
    else
        printf '%s\n' "$(npm_user_prefix)"
    fi
}

npm_known_user_prefixes() {
    local prefix
    for prefix in "$(npm_user_prefix)" "$(npm_config_prefix)" "$HOME/.local" "$HOME/.npm-global"; do
        [[ -n "$prefix" ]] || continue
        npm_prefix_is_under_home "$prefix" || continue
        printf '%s\n' "$prefix"
    done | python3 -c 'import os,sys; seen=set();
for line in sys.stdin:
    value=os.path.normpath(line.strip())
    if value and value not in seen:
        seen.add(value); print(value)'
}

persist_npm_bin_path_if_user_managed() {
    local prefix="$1"
    local bin_dir="$prefix/bin"

    npm_prefix_is_under_home "$prefix" || return 0
    persist_user_bin_path >/dev/null || true
    if ! path_has_dir "$bin_dir"; then
        echo -e "${GREEN}  Added ${bin_dir} to your shell PATH for future sessions.${NC}"
    fi
}

cleanup_duplicate_npm_package_prefixes() {
    local display_name="$1"
    local package="$2"
    local binary="$3"
    local target_prefix="$4"
    local prefix version

    target_prefix="$(python3 -c 'import os,sys; print(os.path.normpath(sys.argv[1]))' "$target_prefix")"
    while IFS= read -r prefix; do
        [[ -n "$prefix" && "$prefix" != "$target_prefix" ]] || continue
        version=$(read_npm_package_version "$package" "$prefix")
        [[ -n "$version" ]] || continue
        [[ -e "$prefix/bin/$binary" || -L "$prefix/bin/$binary" ]] || continue

        echo -e "${YELLOW}  Removing duplicate ${display_name} ${version} from $prefix to prevent PATH shadowing.${NC}"
        if $VERBOSE; then
            npm uninstall -g --prefix "$prefix" "$package" || true
        else
            npm uninstall -g --prefix "$prefix" "$package" --loglevel error >/dev/null 2>&1 || true
        fi
    done < <(npm_known_user_prefixes)
}

npm_prefixes_for_target() {
    local target_prefix="$1"

    {
        printf '%s\n' "$target_prefix"
        npm_config_prefix
        npm_known_user_prefixes
    } | python3 -c 'import os,sys; seen=set();
for line in sys.stdin:
    value=os.path.normpath(line.strip())
    if value and value not in seen:
        seen.add(value); print(value)'
}

npm_prefix_is_writable_for_uninstall() {
    local prefix="$1"
    local global_root="$prefix/lib/node_modules"
    local bin_dir="$prefix/bin"

    [[ -d "$global_root" && -w "$global_root" ]] || return 1
    [[ ! -d "$bin_dir" || -w "$bin_dir" ]]
}

prompt_for_sudo() {
    local action="$1"

    if ! command -v sudo >/dev/null 2>&1; then
        echo -e "${RED}  sudo is required to $action, but sudo was not found.${NC}"
        return 1
    fi

    echo -e "${YELLOW}  sudo is required to $action.${NC}"
    if [[ ! -t 0 ]]; then
        echo -e "${YELLOW}  Run this command from an interactive terminal to enter your sudo password.${NC}"
        return 1
    fi

    sudo -v
}

ai_cli_skip_dependency_install() {
    [[ "${AI_CLI_SKIP_DEPENDENCY_INSTALL:-0}" == "1" ]]
}

ai_cli_package_manager() {
    if command -v pacman >/dev/null 2>&1; then
        printf 'pacman\n'
    elif command -v apt-get >/dev/null 2>&1; then
        printf 'apt\n'
    elif command -v dnf >/dev/null 2>&1; then
        printf 'dnf\n'
    elif command -v brew >/dev/null 2>&1; then
        printf 'brew\n'
    else
        printf 'none\n'
    fi
}

ai_cli_packages_for_dependency() {
    local manager="$1"
    local dependency="$2"

    case "$manager:$dependency" in
        pacman:curl) printf 'curl\n' ;;
        pacman:git) printf 'git\n' ;;
        pacman:python3) printf 'python\n' ;;
        pacman:npm) printf 'nodejs\nnpm\n' ;;
        pacman:gh) printf 'github-cli\n' ;;
        pacman:unzip) printf 'unzip\n' ;;
        pacman:just) printf 'just\n' ;;
        apt:curl) printf 'curl\n' ;;
        apt:git) printf 'git\n' ;;
        apt:python3) printf 'python3\n' ;;
        apt:npm) printf 'nodejs\nnpm\n' ;;
        apt:gh) printf 'gh\n' ;;
        apt:unzip) printf 'unzip\n' ;;
        apt:just) printf 'just\n' ;;
        dnf:curl) printf 'curl\n' ;;
        dnf:git) printf 'git\n' ;;
        dnf:python3) printf 'python3\n' ;;
        dnf:npm) printf 'nodejs\nnpm\n' ;;
        dnf:gh) printf 'gh\n' ;;
        dnf:unzip) printf 'unzip\n' ;;
        dnf:just) printf 'just\n' ;;
        brew:curl) printf 'curl\n' ;;
        brew:git) printf 'git\n' ;;
        brew:python3) printf 'python\n' ;;
        brew:npm) printf 'node\n' ;;
        brew:gh) printf 'gh\n' ;;
        brew:unzip) printf 'unzip\n' ;;
        brew:just) printf 'just\n' ;;
        *) return 1 ;;
    esac
}

ai_cli_install_packages() {
    local manager="$1"
    shift
    local -a packages=("$@")

    [[ ${#packages[@]} -gt 0 ]] || return 0

    if ai_cli_skip_dependency_install; then
        echo -e "${YELLOW}  Dependency install skipped by AI_CLI_SKIP_DEPENDENCY_INSTALL=1.${NC}"
        return 1
    fi

    case "$manager" in
        pacman)
            prompt_for_sudo "install ${packages[*]}" || return 1
            if $VERBOSE; then
                sudo pacman -Sy --noconfirm --needed "${packages[@]}"
            else
                sudo pacman -Sy --noconfirm --needed "${packages[@]}" >/dev/null
            fi
            ;;
        apt)
            prompt_for_sudo "install ${packages[*]}" || return 1
            if $VERBOSE; then
                sudo apt-get update
                sudo apt-get install -y "${packages[@]}"
            else
                sudo apt-get update -qq >/dev/null
                sudo apt-get install -y -qq "${packages[@]}" >/dev/null
            fi
            ;;
        dnf)
            prompt_for_sudo "install ${packages[*]}" || return 1
            if $VERBOSE; then
                sudo dnf install -y "${packages[@]}"
            else
                sudo dnf install -y -q "${packages[@]}" >/dev/null
            fi
            ;;
        brew)
            if $VERBOSE; then
                brew install "${packages[@]}"
            else
                brew install "${packages[@]}" >/dev/null
            fi
            ;;
        *)
            echo -e "${YELLOW}  No supported package manager found for ${packages[*]}.${NC}"
            return 1
            ;;
    esac
}

ensure_dependency_package() {
    local dependency="$1"
    local display_name="$2"
    local manager
    local package packages_text
    local -a packages=()

    manager="$(ai_cli_package_manager)"
    if [[ "$manager" == "apt" && "$dependency" == "gh" ]]; then
        ensure_command_dependency curl curl "curl" || return 1
        if [[ ! -f /etc/apt/sources.list.d/github-cli.list || ! -f /usr/share/keyrings/githubcli-archive-keyring.gpg ]]; then
            prompt_for_sudo "configure the GitHub CLI apt repository" || return 1
            curl -fsSL https://cli.github.com/packages/githubcli-archive-keyring.gpg \
                | sudo dd of=/usr/share/keyrings/githubcli-archive-keyring.gpg >/dev/null 2>&1
            sudo chmod go+r /usr/share/keyrings/githubcli-archive-keyring.gpg
            echo "deb [arch=$(dpkg --print-architecture) signed-by=/usr/share/keyrings/githubcli-archive-keyring.gpg] https://cli.github.com/packages stable main" \
                | sudo tee /etc/apt/sources.list.d/github-cli.list >/dev/null
        fi
    fi

    if ! packages_text=$(ai_cli_packages_for_dependency "$manager" "$dependency"); then
        echo -e "${YELLOW}  Could not map $display_name to a package for this system.${NC}"
        return 1
    fi
    while IFS= read -r package; do
        [[ -n "$package" ]] && packages+=("$package")
    done <<< "$packages_text"

    echo -e "${YELLOW}  $display_name is missing, installing...${NC}"
    ai_cli_install_packages "$manager" "${packages[@]}"
}

ensure_command_dependency() {
    local command_name="$1"
    local dependency="$2"
    local display_name="$3"

    if command -v "$command_name" >/dev/null 2>&1; then
        return 0
    fi

    ensure_dependency_package "$dependency" "$display_name" || return 1
    command -v "$command_name" >/dev/null 2>&1
}

ensure_bun_dependency() {
    prepend_path_dir "$HOME/.bun/bin"
    if command -v bun >/dev/null 2>&1; then
        return 0
    fi

    echo -e "${YELLOW}  Bun is missing, installing...${NC}"
    if ai_cli_skip_dependency_install; then
        echo -e "${YELLOW}  Dependency install skipped by AI_CLI_SKIP_DEPENDENCY_INSTALL=1.${NC}"
        return 1
    fi

    ensure_command_dependency curl curl "curl" || return 1
    if [[ "$(uname -s)" == Linux* ]]; then
        ensure_command_dependency unzip unzip "unzip" || return 1
    fi

    if $VERBOSE; then
        curl -fsSL https://bun.com/install | bash
    else
        curl -fsSL https://bun.com/install 2>/dev/null | bash >/dev/null
    fi

    prepend_path_dir "$HOME/.bun/bin"
    command -v bun >/dev/null 2>&1
}

ensure_just_dependency() {
    if command -v just >/dev/null 2>&1; then
        return 0
    fi

    if [[ "${AI_CLI_JUST_CHECKED:-0}" == "1" ]]; then
        return 0
    fi
    AI_CLI_JUST_CHECKED=1

    ensure_dependency_package just "just" || {
        echo -e "${YELLOW}  just is still missing; Pi vs Claude Code dependencies can install, but recipes will not run until just is installed.${NC}"
        return 0
    }

    if ! command -v just >/dev/null 2>&1; then
        echo -e "${YELLOW}  just is still missing; Pi vs Claude Code dependencies can install, but recipes will not run until just is installed.${NC}"
    fi
}

ensure_update_dependencies() {
    local failed=false
    local checked=false

    echo -e "${CYAN}==> Checking ai-cli dependencies...${NC}"

    ensure_command_dependency python3 python3 "Python 3" || failed=true

    if ai_cli_enabled update claude || ai_cli_enabled update antigravity || ai_cli_enabled update hermes || ai_cli_enabled update omx; then
        ensure_command_dependency curl curl "curl" || failed=true
        checked=true
    fi

    if ai_cli_enabled update pi_vs_claude_code || ai_cli_enabled update hermes; then
        ensure_command_dependency git git "Git" || failed=true
        checked=true
    fi

    if ai_cli_enabled update codex || ai_cli_enabled update pi || ai_cli_enabled update omx; then
        ensure_command_dependency npm npm "Node.js/npm" || failed=true
        checked=true
    fi

    if ai_cli_enabled update gh_cli || ai_cli_enabled update copilot; then
        ensure_command_dependency gh gh "GitHub CLI" || failed=true
        checked=true
    fi

    if ai_cli_enabled update pi_vs_claude_code; then
        ensure_bun_dependency || failed=true
        ensure_just_dependency || true
        checked=true
    fi

    if ! $failed; then
        if $checked; then
            echo -e "${GREEN}  Dependencies ready.${NC}"
        else
            echo -e "${GREEN}  No selected tools require dependency checks.${NC}"
        fi
    fi

    ! $failed
}

npm_uninstall_package_from_prefix() {
    local prefix="$1"
    local package="$2"
    local npm_bin
    local -a cmd

    npm_bin="$(command -v npm 2>/dev/null || true)"
    if [[ -z "$npm_bin" ]]; then
        echo -e "${RED}  npm is required to remove $package.${NC}"
        return 1
    fi

    cmd=("$npm_bin" uninstall -g --prefix "$prefix" "$package")
    if ! $VERBOSE; then
        cmd+=(--loglevel error)
    fi

    if npm_prefix_is_writable_for_uninstall "$prefix"; then
        if $VERBOSE; then
            "${cmd[@]}"
        else
            "${cmd[@]}" >/dev/null
        fi
        return
    fi

    if ! prompt_for_sudo "change npm packages in $prefix"; then
        return 1
    fi

    if $VERBOSE; then
        sudo "${cmd[@]}"
    else
        sudo "${cmd[@]}" >/dev/null
    fi
}

cleanup_legacy_npm_package_prefixes() {
    local display_name="$1"
    local package="$2"
    local binary="$3"
    local target_prefix="$4"
    shift 4
    local -a legacy_packages=("$@")
    local legacy_package prefix version

    [[ ${#legacy_packages[@]} -gt 0 ]] || return 0

    for legacy_package in "${legacy_packages[@]}"; do
        [[ -n "$legacy_package" && "$legacy_package" != "$package" ]] || continue
        while IFS= read -r prefix; do
            [[ -n "$prefix" ]] || continue
            version=$(read_npm_package_version "$legacy_package" "$prefix")
            [[ -n "$version" ]] || continue
            [[ -e "$prefix/bin/$binary" || -L "$prefix/bin/$binary" ]] || continue

            echo -e "${YELLOW}  Removing legacy ${display_name} ${version} from $prefix to free $binary.${NC}"
            if ! npm_uninstall_package_from_prefix "$prefix" "$legacy_package"; then
                return 1
            fi
        done < <(npm_prefixes_for_target "$target_prefix")
    done
}

ensure_npm_binary_path() {
    local display_name="$1"
    local package="$2"
    local binary="$3"
    local target_prefix="$4"
    local target_bin="$target_prefix/bin"
    local resolved=""

    mkdir -p "$target_bin"
    persist_npm_bin_path_if_user_managed "$target_prefix"
    prepend_path_dir "$target_bin"
    cleanup_duplicate_npm_package_prefixes "$display_name" "$package" "$binary" "$target_prefix"

    resolved=$(command -v "$binary" 2>/dev/null || true)
    if [[ -n "$resolved" && "$resolved" != "$target_bin/$binary" ]]; then
        echo -e "${YELLOW}  WARNING: $binary resolves to $resolved, not $target_bin/$binary.${NC}"
        echo -e "${YELLOW}  Check your PATH for another earlier install.${NC}"
    fi
}

install_npm_package() {
    local display_name="$1"
    local package="$2"
    local binary="$3"
    shift 3
    local -a legacy_packages=("$@")
    local target_prefix
    local -a cmd=(npm install -g "${package}@latest" --no-fund)

    target_prefix="$(npm_install_prefix)"
